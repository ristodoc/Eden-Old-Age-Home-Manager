    private void refreshButtonActionPerformed(java.awt.event.ActionEvent evt) {                                              
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/eden","root","");
            
            // Fetch and display employees
            String employeeQuery = "SELECT user_id, name, ward FROM users WHERE role = 'WardEmployee'";
            try (PreparedStatement pst = con1.prepareStatement(employeeQuery)) {
                ResultSet rs = pst.executeQuery();
                DefaultTableModel employeeModel = (DefaultTableModel) employeeTable.getModel();
                employeeModel.setRowCount(0); // Clear existing rows
                
                while (rs.next()) {
                    Vector<String> row = new Vector<>();
                    row.add(rs.getString("user_id"));
                    row.add(rs.getString("name"));
                    row.add(rs.getString("ward"));
                    employeeModel.addRow(row);
                }
            }
            
            // Fetch and display medications
            String medicationQuery = "SELECT medication_id, name, quantity_left FROM medications";
            try (PreparedStatement pst = con1.prepareStatement(medicationQuery)) {
                ResultSet rs = pst.executeQuery();
                DefaultTableModel inventoryModel = (DefaultTableModel) inventoryTable.getModel();
                inventoryModel.setRowCount(0); // Clear existing rows
                
                while (rs.next()) {
                    Vector<String> row = new Vector<>();
                    row.add(rs.getString("medication_id"));
                    row.add(rs.getString("name"));
                    row.add(rs.getString("quantity_left"));
                    inventoryModel.addRow(row);
                }
            }
            
            con1.close();
            
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error refreshing data: " + ex.getMessage());
            logger.log(java.util.logging.Level.SEVERE, null, ex);
        }
    }

