    private void loadBudgetData() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/eden","root","");
            
            // Get admin ward (Z) balance
            String adminQuery = "SELECT assigned_amount FROM wards WHERE ward_id = 'Z'";
            try (PreparedStatement pst = con1.prepareStatement(adminQuery)) {
                ResultSet rs = pst.executeQuery();
                if (rs.next()) {
                    balanceLabel.setText(rs.getString("assigned_amount"));
                }
            }
            
            // Get all ward budgets except admin ward
            String wardQuery = "SELECT ward_id, name, assigned_amount FROM wards WHERE ward_id != 'Z'";
            try (PreparedStatement pst = con1.prepareStatement(wardQuery)) {
                ResultSet rs = pst.executeQuery();
                DefaultTableModel budgetModel = (DefaultTableModel) budgetTable.getModel();
                budgetModel.setRowCount(0); // Clear existing rows
                
                while (rs.next()) {
                    Vector<String> row = new Vector<>();
                    row.add(rs.getString("ward_id"));
                    row.add(rs.getString("name"));
                    row.add(rs.getString("assigned_amount"));
                    budgetModel.addRow(row);
                }
            }
            
            con1.close();
            
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error loading budget data: " + ex.getMessage());
            logger.log(java.util.logging.Level.SEVERE, null, ex);
        }
    }

